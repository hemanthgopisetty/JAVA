class BlockText {
 
    public static void main(String[] args) {
        int x,y;

        y=20;

        for(x=0;x<y;x++)
        {// begin a block
            System.out.println("This is x: " + x); 
            System.out.println("This is y: " + y); 
            y = y - 2;

        }//end of a block
    }
    
 }